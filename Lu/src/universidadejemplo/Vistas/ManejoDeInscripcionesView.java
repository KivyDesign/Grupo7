    private void  llenarComboBox(){
        AlumnoData alumd = new AlumnoData();
        List<Alumno> listaAlumnos = alumd.listarAlumnos();
       
        JCBalumno.removeAllItems();
        
        for (int i = 0 ; i < listaAlumnos.size(); i++){
        JCBalumno.addItem(String.valueOf(listaAlumnos.get(i).getDni())+ ", "+ listaAlumnos.get(i).getApellido()+", "+listaAlumnos.get(i).getNombre()) ;
    }
    }

