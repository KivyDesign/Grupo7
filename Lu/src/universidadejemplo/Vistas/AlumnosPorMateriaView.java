    private void  llenarComboBox(){
        
        MateriaData materiad = new MateriaData();
        List<Materia> listaMaterias = materiad.listarMaterias();
        
        JCBmateria.removeAllItems();
        
        for(int i = 0; i < listaMaterias.size(); i++){
        JCBmateria.addItem(String.valueOf(listaMaterias.get(i).getNombre())+", AÃ±o: "+ listaMaterias.get(i).getAnio());
        }
        
}
    
    private void armarCabeceras(){
        modelo.addColumn("ID Alumno");
        modelo.addColumn("DNI");
        modelo.addColumn("Apellido");
        modelo.addColumn("Nombre");
        JTtablaAPmateria.setModel(modelo);
    }
